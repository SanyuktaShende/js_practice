let integerNumber = 3;
console.log(typeof(integerNumber));    // number

let floatNumber = 3.14;
console.log(typeof(floatNumber));       //number

let output = integerNumber + floatNumber;
// Result will be a floating-point number

console.log(output);    // 6.14
